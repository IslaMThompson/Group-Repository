package com.napier.sem;

/**
 * Represents A Country
 */
public class Country {

    /**
     * Country Code
     */
    public String code;

    /**
     * Country Name
     */
    public String name;

    /**
     * Country Continent
     */
    public String continent;

    /**
     * Country Region
     */
    public String region;

    /**
     * Country Surface Area
     */
    public double surface_area;

    /**
     * Country Independence Year
     */
    public int indep_year;

    /**
     * Country Population
     */
    public int population;

    /**
     * Country Life Expectancy
     */
    public double life_expectancy;

    /**
     * Country Gross National Product (GNP)
     */
    public double gnp;

    /**
     * Country Gross National Product (GNP - OLD)
     */
    public double gnp_old;

    /**
     * Country Local Name
     */
    public String local_name;

    /**
     * Country Government Form
     */
    public String government_form;

    /**
     * Country Head of State
     */
    public String state_head;

    /**
     * Country Capital
     */
    public int capital;

    /**
     * Country Code 2
     */
    public String code2;
}

