package com.napier.sem;

/**
 * Represents A City
 */
public class City {

    /**
     * City ID
     */
    public int id;

    /**
     * City Name
     */
    public String name;

    /**
     * Country Code
     */
    public String country_code;

    /**
     * City District Name
     */
    public String district;

    /**
     * City Population
     */
    public int population;

    /**
     * Country Name
     */
    public String country;
}
